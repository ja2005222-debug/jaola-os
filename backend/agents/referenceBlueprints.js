// ─── المطابقة ───────────────────────────────────────────────────────
export function matchBlueprint(goal = '') {
    const t = (goal || '').toLowerCase();
    if (!t.trim()) return null;
    let best = null;
    let bestScore = 0;
    for (const bp of BLUEPRINTS) {
        const score = bp.keywords.reduce((n, kw) => n + (t.includes(kw.toLowerCase()) ? 1 : 0), 0);
        if (score > bestScore) { bestScore = score; best = bp; }
    }
    return bestScore > 0 ? best : null;
}

// ─── بناء كتلة البرومبت للمولّد ─────────────────────────────────────
export function buildBlueprintPrompt(goal = '') {
    const bp = typeof goal === 'object' && goal ? goal : matchBlueprint(goal);
    if (!bp) return '';

    const lines = [];
    lines.push(`## 🏛️ معمارية مرجعية — النمط: **${bp.label}** (${bp.reference})`);
    lines.push('حاكِ هذه البنية المُثبتة (النمط لا العلامة التجارية) — ابنِ كل عنصر ليعمل فعلاً لا كواجهة ثابتة:');
    if (bp.roles?.length) {
        lines.push(`\n**الأدوار (افصل بينها بصلاحيات وصفحات مستقلة):** ${bp.roles.join('، ')}`);
    }
    lines.push('\n**الصفحات/الأقسام المطلوبة:**');
    bp.pages.forEach((p, i) => lines.push(`${i + 1}. ${p}`));
    lines.push('\n**المكوّنات الوظيفية (تعمل بـ JS ومصفوفات بيانات واقعية، ليست عناصر عرض فقط):**');
    bp.components.forEach(c => lines.push(`- ${c}`));

    const libs = (bp.libs || []).map(id => CURATED_LIBS[id]).filter(Boolean);
    if (libs.length) {
        lines.push('\n**مكتبات مسموحة لهذا النمط فقط (CDN، حمّلها بـ defer/كسولاً، لهذه القدرة تحديداً — لا تضف غيرها):**');
        for (const lib of libs) {
            lines.push(`- ${lib.name}: ${lib.why}.${lib.css ? ` CSS: ${lib.css}` : ''} JS: ${lib.js}`);
        }
    } else {
        lines.push('\n**بلا مكتبات خارجية** — Vanilla JS كافٍ لهذا النمط (أخفّ وأسرع = أداء أعلى).');
    }
    return lines.join('\n');
}
